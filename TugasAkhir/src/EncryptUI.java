    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        ElGamalAlgorithm x = new ElGamalAlgorithm();
        
        BigInteger prime = BigInteger.valueOf(Long.valueOf(jTextField4.getText()));
        BigInteger alpha = BigInteger.valueOf(Long.valueOf(jTextField1.getText()));
        BigInteger beta = BigInteger.valueOf(Long.valueOf(jTextField6.getText()));
        
        
        char[] plaintext = (jTextField3.getText()).toCharArray();
        int[] int_pt = new int[plaintext.length*2];
        String pt = "";
        
        //long s = System.nanoTime();
        for (int i=0, j=0; i<plaintext.length; i++, j+=2) {
            //System.out.println((int) plaintext[i]);
            BigInteger[] bigint_pt = x.Encrypt((int) plaintext[i], prime, alpha, beta);
            //System.out.print(bigint_pt[0]+" "+bigint_pt[1]+" \n");
            int_pt[j] = bigint_pt[0].intValue();
            int_pt[j+1] = bigint_pt[1].intValue();
            if (pt.equals("")) {
                pt = pt+Integer.toString(int_pt[j])+"_"+Integer.toString(int_pt[j+1]);
            }
            else {
                pt = pt+"_"+Integer.toString(int_pt[j])+"_"+Integer.toString(int_pt[j+1]);
            }
        }
        //long f = System.nanoTime();
        //System.out.println(f-s+" nanoseconds");
        jTextField5.setText(pt);
    }//GEN-LAST:event_jButton8ActionPerformed


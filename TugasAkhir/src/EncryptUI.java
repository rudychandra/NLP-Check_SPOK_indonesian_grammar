    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter txtfilter = new FileNameExtensionFilter("Text Files", "txt");
        FileNameExtensionFilter docxfilter = new FileNameExtensionFilter("Microsoft Word 2007-2010 Documents", "docx");
        fc.setAcceptAllFileFilterUsed(false);
        fc.setFileFilter(docxfilter);
        fc.addChoosableFileFilter(txtfilter);
        int returnVal = fc.showSaveDialog(jPanel1);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            if (file.getName().endsWith("docx")){
                if (Desktop.isDesktopSupported()){
                    try {
                        XWPFDocument document = new XWPFDocument();
                        FileOutputStream out = new FileOutputStream(file);
                        XWPFParagraph par = document.createParagraph();
                        XWPFRun run = par.createRun();
                        run.setText("Ciphertext");
                        par = document.createParagraph();
                        run = par.createRun();
                        run.setText(jTextArea2.getText());
                        par = document.createParagraph();
                        run = par.createRun();
                        run.setText("Encrypted session key");
                        par = document.createParagraph();
                        run = par.createRun();
                        run.setText(jTextField5.getText());
                        par = document.createParagraph();
                        run = par.createRun();
                        run.setText("----------------------");
                        document.write(out);
                        out.close();
                        Desktop.getDesktop().open(file);
                    } catch (IOException ex) {
                        Logger.getLogger(EncryptUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else {
                    JOptionPane.showMessageDialog(null, "Your PC Unsupported", "ERROR", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else {
                try {
                    BufferedWriter o = new BufferedWriter(new FileWriter(file));
                    o.write("Ciphertext"+"\n"+
                            jTextArea2.getText()+"\n"+"\n"+
                            "Encrypted session key"+"\n"+
                            jTextField5.getText());
                    o.close();
                    Desktop.getDesktop().open(file);
                } catch (IOException ex) {
                    Logger.getLogger(EncryptUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

